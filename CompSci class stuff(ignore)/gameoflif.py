def iteration(neighborCounter,baseList):
    #generate a list of the next iteration of the game
    for list in range (0,30):
        for j in range (0,30):
            counter=neighborCounter[list][j]
            if baseList[list][j]==1 and counter<2:
                baseList[list][j]=0
            elif baseList[list][j]==1 and counter>3:
                baseList[list][j]=0
            elif baseList[list][j]==0 and counter==3:
                baseList[list][j]=1
    return baseList

def changeState(grid,baseList):
    #change the gameboard to represent the next iteration
    neighborCounter=[]
    for list in range(0,30):
        nextList=[]
        for j in range(0,30):
            nextList.append(neighbours(baseList,list,j))
        neighborCounter.append(nextList)
    baseList=iteration(neighborCounter,baseList)
    for list in range (0,30):
        for j in range (0,30):
            if baseList[list][j]==1:
                cells[list][j]["bg"]="black"
            else:
                cells[list][j]["bg"]="white"

