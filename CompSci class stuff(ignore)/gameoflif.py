from Tkinter import *
def neighbours(baseList, list, j):
    #generate a list of how many neighbors each cell has
    counter=0
    if list-1>=0:
        if baseList[list-1][j]==1:
            counter+=1
        if j+1<=29:
            if baseList[list-1][j+1]==1:
                counter+=1
        if j-1>=0:
            if baseList[list-1][j-1]==1:
                counter+=1
    if list+1<=29:
        if baseList[list+1][j]==1:
            counter+=1
        if j+1<=29:
            if baseList[list+1][j+1]==1:
                counter+=1
        if j-1>=0:
            if baseList[list+1][j-1]==1:
                counter+=1
    if j+1<=29:
        if baseList[list][j+1]==1:
            counter+=1
    if j-1>=0:
        if baseList[list][j-1]==1:
            counter+=1
    return counter


